export function matchPalette(input) {
  if (!input.trim()) return palettes[Math.floor(Math.random() * palettes.length)]

  const words = input.toLowerCase().split(/\s+/)
  const scored = palettes.map(p => {
    let score = 0
    for (const word of words) {
      for (const tag of p.tags) {
        if (tag.includes(word) || word.includes(tag)) score++
      }
    }
    return { palette: p, score }
  })

  scored.sort((a, b) => b.score - a.score)
  const top = scored.filter(s => s.score === scored[0].score)
  return top[Math.floor(Math.random() * top.length)].palette
}
